a = "python".count("p")
print(a)

a = "python".find("y")
print(a)

a = "python".index("yt")
print(a)

a = "python".isalnum()
print(a)

a = "python".isalpha()
print(a)

a = "python".isascii()
print(a)

a = "python".isdecimal()
print(a)

a = "python".isdigit()
print(a)

a = "python".isidentifier()
print(a)

a = "python".islower()
print(a)

a = "python".isnumeric()
print(a)

a = "python".isprintable()
print(a)